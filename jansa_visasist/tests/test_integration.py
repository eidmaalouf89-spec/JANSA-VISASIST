# Layer 1 — Smoke tests
# ═════════════════════════════════════════════════════════════════

class TestSmoke:
    """Basic pipeline health checks."""

    def test_pipeline_completes(self, pipeline_result):
        master, ctx, _ = pipeline_result
        assert ctx.total_rows > 0

    def test_all_outputs_created(self, pipeline_result):
        _, _, tmpdir = pipeline_result
        expected = [
            "master_dataset.json",
            "master_dataset.csv",
            "import_log.json",
            "import_log.csv",
            "header_mapping_report.json",
            "validation_report.json",
        ]
        for fname in expected:
            assert os.path.exists(os.path.join(tmpdir, fname)), f"Missing: {fname}"

    def test_no_silent_row_drops(self, pipeline_result):
        master, ctx, _ = pipeline_result
        assert len(master) == ctx.total_rows
        assert sum(ctx.sheet_row_counts.values()) == ctx.total_rows

    def test_every_row_has_traceability(self, pipeline_result):
        master, _, _ = pipeline_result
        for row in master:
            assert row["source_sheet"] is not None, f"row {row.get('row_id')}"
            assert row["source_row"] is not None, f"row {row.get('row_id')}"
            assert row["row_id"] is not None
            assert row["row_quality"] in ("OK", "WARNING", "ERROR")

    def test_gp2_no_empty_strings(self, pipeline_result):
        master, _, _ = pipeline_result
        normalized_fields = [
            "document", "visa_global", "ind",
            "date_diffusion", "date_reception", "date_contractuelle_visa",
        ]
        for row in master:
            for field in normalized_fields:
                val = row.get(field)
                if val is not None:
                    assert val != "", \
                        f"GP2 violation: empty string in {field}, row {row.get('row_id')}"


