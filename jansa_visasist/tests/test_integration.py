"""
Integration tests: End-to-end pipeline with known GrandFichier.

Three layers:
  1. Smoke tests (pipeline runs, outputs exist, traceability, GP2)
  2. Golden semantic regression (row count, schema, per-sheet counts,
     quality distribution, sample rows, row fingerprints)
  3. Golden validation + log regression

Golden files are generated by:
    python -m jansa_visasist.tests.generate_golden
"""

import hashlib
import json
import logging
import os
import tempfile
import warnings
from collections import Counter

import pytest

from jansa_visasist.context import PipelineContext
from jansa_visasist.main import run_pipeline


# ── Path resolution ──────────────────────────────────────────────

_PROJECT_ROOT = os.path.normpath(os.path.join(os.path.dirname(__file__), "..", ".."))
_DEFAULT_PATH = os.path.join(_PROJECT_ROOT, "source", "GrandFichier.xlsx")
REFERENCE_FILE = os.environ.get("JANSA_TEST_GRANDFICHIER", _DEFAULT_PATH)

GOLDEN_DIR = os.path.join(os.path.dirname(__file__), "golden")
GOLDEN_SNAPSHOT = os.path.join(GOLDEN_DIR, "golden_snapshot.json")
GOLDEN_VALIDATION = os.path.join(GOLDEN_DIR, "golden_validation.json")
GOLDEN_LOG_SUMMARY = os.path.join(GOLDEN_DIR, "golden_log_summary.json")


# ── Helpers ──────────────────────────────────────────────────────

def _row_fingerprint(row: dict) -> str:
    """Must match generate_golden._row_fingerprint exactly."""
    parts = [
        str(row.get("row_id", "")),
        str(row.get("source_sheet", "")),
        str(row.get("source_row", "")),
        str(row.get("document", "")),
        str(row.get("document_raw", "")),
        str(row.get("ind", "")),
        str(row.get("visa_global", "")),
        str(row.get("row_quality", "")),
        str(row.get("date_diffusion", "")),
        str(row.get("date_reception", "")),
        str(row.get("titre", ""))[:80],
    ]
    raw = "|".join(parts)
    return hashlib.sha256(raw.encode("utf-8")).hexdigest()[:16]


def _load_golden(path):
    """Load a golden JSON file, return None if missing."""
    if not os.path.exists(path):
        return None
    with open(path, encoding="utf-8") as f:
        return json.load(f)


# ── Shared fixture: single pipeline run for all tests ────────────

@pytest.fixture(scope="module")
def pipeline_result():
    """
    Run the pipeline once and return (master_data, ctx, tmpdir_path).
    Shared across all tests in this module to avoid re-running the
    ~12s pipeline for each test.
    """
    if not os.path.exists(REFERENCE_FILE):
        pytest.skip(f"Reference GrandFichier not found at {REFERENCE_FILE}")

    tmpdir = tempfile.mkdtemp(prefix="jansa_golden_")
    ctx = PipelineContext(input_path=REFERENCE_FILE, output_dir=tmpdir)

    # Suppress openpyxl's noisy UserWarnings about data validation / styles
    with warnings.catch_warnings():
        warnings.filterwarnings("ignore", category=UserWarning, module="openpyxl")
        exit_code = run_pipeline(ctx)

    assert exit_code == 0, f"Pipeline failed with exit code {exit_code}"

    with open(os.path.join(tmpdir, "master_dataset.json"), encoding="utf-8") as f:
        master = json.load(f)

    return master, ctx, tmpdir


