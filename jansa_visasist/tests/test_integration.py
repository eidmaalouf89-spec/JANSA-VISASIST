class TestGoldenSnapshot:
    """Compare pipeline output against the golden semantic snapshot."""

    @pytest.fixture(autouse=True)
    def _load_snapshot(self):
        self.snapshot = _load_golden(GOLDEN_SNAPSHOT)
        if self.snapshot is None:
            pytest.skip(
                "Golden snapshot not found. Generate with: "
                "python -m jansa_visasist.tests.generate_golden"
            )

    def test_row_count(self, pipeline_result):
        master, _, _ = pipeline_result
        expected = self.snapshot["total_rows"]
        assert len(master) == expected, \
            f"Row count changed: expected {expected}, got {len(master)}"

    def test_schema_columns(self, pipeline_result):
        master, _, _ = pipeline_result
        actual_schema = sorted(master[0].keys()) if master else []
        expected_schema = self.snapshot["schema"]
        assert actual_schema == expected_schema, \
            f"Schema mismatch.\nAdded: {set(actual_schema) - set(expected_schema)}\n" \
            f"Removed: {set(expected_schema) - set(actual_schema)}"

    def test_per_sheet_row_counts(self, pipeline_result):
        master, _, _ = pipeline_result
        actual_counts = dict(Counter(row["source_sheet"] for row in master))
        expected_counts = self.snapshot["sheet_counts"]
        assert actual_counts == expected_counts, \
            f"Per-sheet row counts changed:\n" \
            f"Expected: {expected_counts}\nActual:   {actual_counts}"

    def test_quality_distribution(self, pipeline_result):
        master, _, _ = pipeline_result
        actual_dist = dict(Counter(row["row_quality"] for row in master))
        expected_dist = self.snapshot["quality_distribution"]
        assert actual_dist == expected_dist, \
            f"Quality distribution changed:\n" \
            f"Expected: {expected_dist}\nActual:   {actual_dist}"

    def test_visa_distribution(self, pipeline_result):
        master, _, _ = pipeline_result
        actual_dist = dict(Counter(str(row.get("visa_global")) for row in master))
        expected_dist = self.snapshot["visa_distribution"]
        assert actual_dist == expected_dist, \
            f"Visa distribution changed:\n" \
            f"Expected: {expected_dist}\nActual:   {actual_dist}"

    def test_header_mapping_summary(self, pipeline_result):
        _, _, tmpdir = pipeline_result
        with open(os.path.join(tmpdir, "header_mapping_report.json"), encoding="utf-8") as f:
            actual_report = json.load(f)

        expected_summary = self.snapshot["header_mapping_summary"]

        # Compare sheet-by-sheet
        actual_summary = []
        for r in actual_report:
            actual_summary.append({
                "sheet": r["sheet"],
                "header_row_detected": r["header_row_detected"],
                "core_columns_mapped": r["core_columns_mapped"],
                "mapping_confidence": r["mapping_confidence"],
                "approvers_detected": r["approvers_detected"],
            })

        assert len(actual_summary) == len(expected_summary), \
            f"Sheet count: {len(actual_summary)} vs {len(expected_summary)}"

        for act, exp in zip(actual_summary, expected_summary):
            assert act == exp, \
                f"Header mapping changed for sheet '{exp['sheet']}':\n" \
                f"Expected: {exp}\nActual:   {act}"

    def test_sample_rows(self, pipeline_result):
        """Spot-check representative rows at known positions."""
        master, _, _ = pipeline_result
        for sample in self.snapshot["sample_rows"]:
            idx = sample["index"]
            assert idx < len(master), f"Sample index {idx} out of range"
            actual_row = master[idx]

            for key in ("row_id", "source_sheet", "source_row", "document",
                        "ind", "visa_global", "row_quality"):
                assert actual_row.get(key) == sample.get(key), \
                    f"Sample row {idx}, field '{key}': " \
                    f"{actual_row.get(key)!r} != {sample.get(key)!r}"

    def test_row_fingerprints(self, pipeline_result):
        """Every row's identity+key-field fingerprint matches golden."""
        master, _, _ = pipeline_result
        expected_fps = self.snapshot["row_fingerprints"]

        assert len(master) == len(expected_fps), "Row count mismatch"

        mismatches = []
        for i, (row, exp) in enumerate(zip(master, expected_fps)):
            actual_fp = _row_fingerprint(row)
            if actual_fp != exp["fp"]:
                mismatches.append(
                    f"  Row {i} (row_id={exp['row_id']}): "
                    f"expected {exp['fp']}, got {actual_fp}"
                )

        assert len(mismatches) == 0, \
            f"{len(mismatches)} row fingerprint(s) changed:\n" + \
            "\n".join(mismatches[:20]) + \
            (f"\n  ... and {len(mismatches) - 20} more" if len(mismatches) > 20 else "")


