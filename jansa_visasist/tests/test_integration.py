class TestGoldenValidation:
    """Compare validation report against golden reference."""

    @pytest.fixture(autouse=True)
    def _load_golden_validation(self, pipeline_result):
        self.golden = _load_golden(GOLDEN_VALIDATION)
        if self.golden is None:
            pytest.skip("Golden validation not found. Run generate_golden.")
        _, _, self.tmpdir = pipeline_result

    def test_validation_checks_match(self):
        with open(os.path.join(self.tmpdir, "validation_report.json"), encoding="utf-8") as f:
            actual = json.load(f)

        for check_name, expected_result in self.golden.items():
            assert check_name in actual, f"Missing validation check: {check_name}"
            assert actual[check_name]["passed"] == expected_result["passed"], \
                f"Validation '{check_name}' changed: " \
                f"expected passed={expected_result['passed']}, " \
                f"got passed={actual[check_name]['passed']}"


